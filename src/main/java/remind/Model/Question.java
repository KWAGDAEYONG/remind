package remind.Model;

public class Question {
	int q_id;
	String q_user;
	String title;
	String content;
	
	public void setQ_id(int q_id) {
		this.q_id = q_id;
	}
	
	public void setQ_user(String q_user) {
		this.q_user = q_user;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}
	
	public void setContent(String content) {
		this.content = content;
	}
	
}
